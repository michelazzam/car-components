/**
 * Constant representing the policy for continuing sales when an item is out of stock.
 *
 * If this constant is set to `true`, the system will allow sales to continue even when the product is out of stock.
 * If set to `false`, sales will be prevented when the product is out of stock.
 *
 * @constant {boolean}
 */
export const CSOS = true; // Continue selling when out of stock

/**
 * Constant representing the ability to print statement.
 *
 * If this constant is set to `true`, the system will allow user to print statement.
 *
 * @constant {boolean}
 */
export const HAS_STATEMENT = false; // Has the ability to select invoices and print statement
